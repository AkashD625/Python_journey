# Lambda functions - These are the short cut methods to write the functon in the shortcut method.
# Normal function
def norfunc(x):
      return x*x
print(norfunc(5))



#Lambda Function 
print("Lambada function")
norfunc = lambda x: x*16
print(norfunc(10))

#Where norfunc - function name
      #  lambda  - fuction keyword
      #  x - parameter
      #  x*6 - returning value